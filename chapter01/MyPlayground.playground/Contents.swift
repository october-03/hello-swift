// 상수는 변하지 않는 값이며 아래와 같이 사용
let a = 100;

// 변수는 변하는 값이며 아래와 같이 사용
var b = 100;
b = 200;

b;

// Int : 64bit 정수형
var int: Int = -100;
int;

// UInt : 부호가 없는 64bit 정수형
var uInt: UInt = 100;

// Float : 32bit 부동 소수점
var float: Float = 1.1;

// Double : 64bit 부동 소수점
var double: Double = 1.1;

// Bool : boolean 값
var bool: Bool = true;

// Character : 문자
var character: Character = "가"

// String : 문자열
var string: String = "가나다"

// Any : 모든 타입을 지칭
var any: Any = 1.1;
any = -1.1;
any = 100;
any = -100;
any = false;
